package cmd

////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"fmt"
	"os"
	"path/filepath"
)

////////////////////////////////////////////////////////////////////////////////////////////////////

// parse repo size
func repoSize(repo string) (string, error) {
	var size int
	err := filepath.Walk(repo, func(_ string, info os.FileInfo, err error) error {
		if err == nil && !info.IsDir() {
			size += int(info.Size())
		}
		return err
	})
	return formatRepoSize(size), err
}

////////////////////////////////////////////////////////////////////////////////////////////////////

func formatRepoSize(size int) string {
	const (
		KB = 1024
		MB = KB * 1024
		GB = MB * 1024
	)

	switch {
	case size >= GB:
		return fmt.Sprintf("%d GB", size/GB)
	case size >= MB:
		return fmt.Sprintf("%d MB", size/MB)
	case size >= KB:
		return fmt.Sprintf("%d KB", size/KB)
	default:
		return fmt.Sprintf("%d bytes", size)
	}
}

