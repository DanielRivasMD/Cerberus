package cmd

////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"fmt"
	"os"
)

////////////////////////////////////////////////////////////////////////////////////////////////////

func createFile(path string) error {
	fmt.Printf("Attempting to create file '%s'.\n", path)
	file, err := os.Create(path)
	if err != nil {
		return fmt.Errorf("failed to create file '%s': %w", path, err)
	}
	defer file.Close()
	fmt.Printf("File '%s' created successfully.\n", path)
	return nil
}

