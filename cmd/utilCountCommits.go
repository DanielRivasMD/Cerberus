package cmd

////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"strconv"
	"strings"
)

////////////////////////////////////////////////////////////////////////////////////////////////////

// counts total commits
func countCommits() (int, error) {
	out, _, ε := captureExecCmd("git", "rev-list", "--count", "HEAD")
	checkErr(ε)

	commits, err := strconv.Atoi(strings.TrimSpace(string(out)))
	return commits, err
}

