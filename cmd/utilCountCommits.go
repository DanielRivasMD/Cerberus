package cmd

////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"strconv"
	"strings"
)

////////////////////////////////////////////////////////////////////////////////////////////////////

// counts total commits
func countCommits(repo string) (int, error) {
	out, _, ε := execCmdCapture("git", "-C", repo, "rev-list", "--count", "HEAD")
	checkErr(ε)

	commits, err := strconv.Atoi(strings.TrimSpace(string(out)))
	return commits, err
}

